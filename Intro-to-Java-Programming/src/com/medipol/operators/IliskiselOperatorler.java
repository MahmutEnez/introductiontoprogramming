package com.medipol.operators;

public class IliskiselOperatorler {

	public static void main(String[] args) {

		int x = 10;
		int y = 4;
		boolean sonuç;
		sonuç = (x > y);
		System.out.println("x > y  = " + sonuç);
		sonuç = (x < y);
		System.out.println("x < y  = " + sonuç);
		sonuç = (x <= y);
		System.out.println("x <= y = " + sonuç);
		sonuç = (x >= y);
		System.out.println("x >= y = " + sonuç);
		sonuç = (x == y);
		System.out.println("x == y = " + sonuç);
		sonuç = (x != y);
		System.out.println("x != y = " + sonuç);
	}

}
